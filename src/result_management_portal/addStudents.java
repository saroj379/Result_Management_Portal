    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        String course = (String)course1.getSelectedItem();
        String branch = (String)branchName.getSelectedItem();
        String roll = roll_number.getText();
        String name = myname.getText();
        String gender = (String)gender1.getSelectedItem();
        String father = fatherName.getText();
        // if the blank space will try to save then it will pop-up a error window with a msg
        if(course.equals("-select-") || branch.equals("-select-") || roll.equals("") || name.equals("") || gender.equals("-select-") || father.equals("")){
            UIManager.put("OptionPane.messageFont", new FontUIResource(new Font("Georgia", Font.BOLD, 14)));
            JOptionPane.showMessageDialog(null, "<html><div color=red>Please input valid data!", "SetColor", JOptionPane.ERROR_MESSAGE);
        }
        // if trying put duplicate value then show error
        // else it will excute
        else{
            try{
                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root","admin");
                
                String qr = "select * from student where rollno='"+roll+"'";
                Statement st = con.createStatement();
                ResultSet rs = st.executeQuery(qr);
                
                if(rs.next()){
                    UIManager.put("OptionPane.messageFont", new FontUIResource(new Font("Georgia", Font.BOLD, 14)));
                    JOptionPane.showMessageDialog(null, "<html><div color=red>Roll no.-'"+roll+"' is Already Exist!", "SetColor", JOptionPane.ERROR_MESSAGE);
                }
                else{
                    st.executeUpdate("insert into student(rollno, course, branchName,name, gender, fatherName) values('"+roll+"', '"+course+"', '"+branch+"', '"+name+"', '"+gender+"', '"+father+"')");
                    UIManager.put("OptionPane.messageFont", new FontUIResource(new Font("Georgia", Font.BOLD, 14)));
                    JOptionPane.showMessageDialog(null, "<html><div color=green>Data save successfully!","success", JOptionPane.INFORMATION_MESSAGE);

                    int a = JOptionPane.showConfirmDialog(null,"Do you want to Add the Marks?");
                    if(a == JOptionPane.YES_OPTION){
                        setVisible(false);
                        new addResult().setVisible(true);
                    }
                    else{
                        setVisible(false);
                        new addStudents().setVisible(true);
                    }
                }
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e.toString());
            }
        }
    }//GEN-LAST:event_saveActionPerformed

