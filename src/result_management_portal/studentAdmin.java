    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        String rollno = roll_number.getText();
        // if blank then error msg
        if(rollno.equals("")){
            UIManager.put("OptionPane.messageFont", new FontUIResource(new Font("Georgia", Font.BOLD, 14)));
            JOptionPane.showMessageDialog(null, "<html><div color=red>Please, Enter Your Roll Number!", "SetColor", JOptionPane.ERROR_MESSAGE);
        }
        // otherwise mission successful
        else{
            try{
                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root", "admin");

                Statement st = con.createStatement();
                ResultSet rs = st.executeQuery("select * from student where rollno='"+rollno+"'");
                if(!rs.next()){
                    System.out.println("no if cond -> '"+rollno+"'");
                    UIManager.put("OptionPane.messageFont", new FontUIResource(new Font("Georgia", Font.BOLD, 14)));
                    JOptionPane.showMessageDialog(null, "<html><div color=blue>Your Roll no. is not valid for this University! Please, Take the Addmision First.", "SetColor", JOptionPane.WARNING_MESSAGE);
                }
                else{
                    System.out.println("no else cond -> '"+rollno+"'");
                    rs = st.executeQuery("select * from result where rollno='"+rollno+"'");
                    if(rs.next()){
                        setVisible(false);
                        new studentIndex(rollno).setVisible(true);
                    }
                    else{
                        UIManager.put("OptionPane.messageFont", new FontUIResource(new Font("Georgia", Font.BOLD, 14)));
                    JOptionPane.showMessageDialog(null, "<html><div color=blue>Your Result is not Publish! Please, contact to your Admin Section.", "SetColor", JOptionPane.WARNING_MESSAGE);
                    }
                }
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e.toString());
            }
        }
    }//GEN-LAST:event_searchActionPerformed

