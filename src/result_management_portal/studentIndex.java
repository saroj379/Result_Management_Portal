    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        String rollno = roll_number.getText();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root", "admin");
            
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student inner join result where student.rollno='"+rollno+"' and result.rollno='"+rollno+"'");
            
            if(rs.next()){
                course.setText(rs.getString(2));
                branch.setText(rs.getString(3));
                name.setText(rs.getString(4));
                father_name.setText(rs.getString(6));
                gender.setText(rs.getString(5));
                ds.setText(rs.getString(8));
                os.setText(rs.getString(9));
                cns.setText(rs.getString(10));
                dbms.setText(rs.getString(11));
                iot.setText(rs.getString(12));
                
                int s1 = Integer.parseInt(ds.getText());
                int s2 = Integer.parseInt(os.getText());
                int s3 = Integer.parseInt(cns.getText());
                int s4 = Integer.parseInt(dbms.getText());
                int s5 = Integer.parseInt(iot.getText());
                
                int result = (s1 + s2 + s3 + s4 + s5);
                
                String totalSecuredMark = String.valueOf(result);
                total_secured_marks.setText(totalSecuredMark);
                
                int grd = result / 5;
                percentage.setText(String.valueOf(grd));
                
                String str;
                if(s1 < 33 || s2 < 33 || s3 < 33 || s4 < 33 || s5 < 33) str = "F";
                else if(grd > 90) str = "A1";
                else if(grd > 80 && grd <= 90) str = "A2";
                else if(grd > 70 && grd <= 80) str = "B1";
                else if(grd > 60 && grd <= 70) str = "B2";
                else if(grd > 50 && grd <= 60) str = "C";
                else if(grd > 40 && grd <= 50) str = "D";
                else if(grd > 33 && grd <= 40) str = "E";
                else str = "F";
                
                grade.setText(str);
            }
            else{
                JOptionPane.showMessageDialog(null, "Check Again!");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_formComponentShown

